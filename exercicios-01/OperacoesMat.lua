--[[
===== Declaração de Variáveis e Operações Aritméticas ====

    -Objetivo: Declarar variáveis e realizar operações básicas.

    -Instruções: Crie três variáveis com números inteiros. Realize as
    operações de soma, subtração, multiplicação e divisão, e exiba o
    resultado de cada operação usando a função print().
]]

--variáriveis
 num01 = 12
 num02 = 42
 num03 = 21

--operações
 soma = num01 + num02 + num03
 subtracao = num01 - num02 - num03
 multiplicacao = num01 * num02 * num03
 divisao = num01 / num02 / num03

print("A soma de 'num01', 'num02', 'num03' resulta em: "..soma)
print("A subtração de 'num01', 'num02', 'num03' resulta em: "..subtracao)
print("A multiplicação de 'num01', 'num02', 'num03' resulta em: "..multiplicacao)
print("A divisão de 'num01', 'num02', 'num03' resulta em: "..divisao)
